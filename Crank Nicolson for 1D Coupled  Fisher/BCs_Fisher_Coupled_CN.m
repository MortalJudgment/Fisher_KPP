%% BCs to 1D Coupled Fisher Using Crank Nicolson Scheme

function[ue,ve]= BCs_Fisher_Coupled_CN(ue,ve,t);

%%=========================================================================
%      Declear Constants
%%=========================================================================
global a  Z0  c L b

%%=========================================================================
%      Declear and Finding of Exact Ue and Ve
%%=========================================================================
X(1)=a;
Z(1)=a-c*t;
K1=(-c/2)+0.5*(sqrt((c^2) +4));
K2=(-c/2)+0.5*(sqrt((c^2) +4));
ue(1)=0.5 + 0.5*tanh(0.5*K1*(Z(1)-Z0));
ve(1)=0.5 - 0.5*tanh(0.5*K2*(Z(1)-Z0));

X(L)=b;
Z(L)=b-c*t;
K1=(-c/2)+0.5*(sqrt((c^2) +4));
K2=(-c/2)+0.5*(sqrt((c^2) +4));
ue(L)=0.5 + 0.5*tanh(0.5*K1*(Z(L)-Z0));
ve(L)=0.5 - 0.5*tanh(0.5*K2*(Z(L)-Z0));

return

