%% Exact Solution to 1D Coupled Fisher Using Crank Nicolson Scheme

function[ue,ve]= Ex_Fisher_Coupled_CN(ue,ve,t);

%%=========================================================================
%      Declear Constants
%%=========================================================================
global L a h Z0  c

%%=========================================================================
%      Declear and Finding of Exact Ue and Ve
%%=========================================================================

for i=1:L
    X(i)=a+(i-1)*h;
    Z(i)=X(i)- c*t;
    K1=(-c/2)+0.5*(sqrt((c^2) +4));
    K2=(-c/2)+0.5*(sqrt((c^2) +4));
    ue(i)=0.5 + 0.5*tanh(0.5*K1*(Z(i)-Z0));
    ve(i)=0.5 - 0.5*tanh(0.5*K2*(Z(i)-Z0));
end

return

%%=========================================================================
%      Exact Solution Programme ended here.
